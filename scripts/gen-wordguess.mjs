const seen = new Set();
const answers = [];
const missing = [];
const dups = [];
const badLen = [];
for (const w of CANDIDATES) {
  if (w.length !== 5) { badLen.push(w); continue; }
  if (seen.has(w)) { dups.push(w); continue; }
  seen.add(w);
  if (!valid.has(w)) { missing.push(w); continue; }
  answers.push(w);
}
answers.sort();

const validSorted = [...valid].sort();

